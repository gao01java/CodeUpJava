package Codeup_1;

import java.util.Scanner;

public class codeup_1139 {
    public static void main(String[]args){
        Scanner in=new Scanner(System.in);

        int a=in.nextInt();
        int b=in.nextInt();

        if(a==1 && b==1){
            System.out.println("1");
        }else if(a==0 && b==0){
            System.out.println("0");
        }else{
            System.out.println("0");
        }
    }
}
